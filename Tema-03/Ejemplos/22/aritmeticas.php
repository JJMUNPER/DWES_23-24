<?php
// Funciones PHP operaciones matematicas
// En los argumentos se debería indicar el tipo de valor que se va a introducir
function sumar(int $var1, int $var2){
    return $var1 + $var2;
}

function restar($var1, $var2){
    return $var1 - $var2;
}

function dividir($var1, $var2){
    return $var1/$var2;
}

function multiplicar($var1, $var2){
    return $var1 * $var2;
}

?>